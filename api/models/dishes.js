// create schema for dishes

var mongoose = require("mongoose");
var Schema = mongoose.Schema;

var dishSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
    },
    price: {
      type: Number,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

var Dishes = mongoose.model("Dishes", dishSchema);
